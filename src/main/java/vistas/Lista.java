    private void B2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_B2ActionPerformed
        // TODO add your handling code here:
        for(Aeropuerto it:conexiones.getAeropuertos())
            {
                if(it.getCodigo()==C1.getSelectedIndex())
                {
                    seleccionado=new Aeropuerto(it.getNombre()
                            ,it.getCodigo());
                    break;
                }
        }
        jFrame1.setVisible(true);
        for(int i=0;i<conexiones.verConexiones(seleccionado).size();i++)
        {
            javax.swing.JLabel E=new javax.swing.JLabel();
            E.setBounds(0, 50+i*20, 400, 50);
            E.setVisible(true);
            E.setText(conexiones.verConexiones(seleccionado).get(i));
            jPanel1.add(E);
        }
    }//GEN-LAST:event_B2ActionPerformed

