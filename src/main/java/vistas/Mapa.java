    private void B1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_B1ActionPerformed
        // TODO add your handling code here:
        jFrame1.setVisible(true);
        HashMap<Map<Aeropuerto,Aeropuerto>,Boolean> cierreTransitivo=new HashMap<>(conexiones.metodoWarshall());
        for(int i=0;i<ruta.size()-1;i++)
        {
            
            if(cierreTransitivo.get(Map.of(ruta.get(i),ruta.get(i+1))))
            {
                javax.swing.JLabel E = new javax.swing.JLabel();
                E.setText("Existe ruta entre:"+ruta.get(i).getNombre()+","
                +ruta.get(i+1).getNombre());
                E.setBounds(0,i*20,400,50);
                E.setVisible(true);
                jPanel2.add(E);
            }
            else
            {
                javax.swing.JLabel E = new javax.swing.JLabel();
                E.setText("No existe ruta entre:"+ruta.get(i).getNombre()+","
                +ruta.get(i+1).getNombre());
                E.setBounds(0,i*20,400,50);
                E.setVisible(true);
                jPanel2.add(E);
                break;
            }
        }
    }//GEN-LAST:event_B1ActionPerformed

