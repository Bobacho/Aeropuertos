    private void BoxesActionPerformed(javax.swing.JCheckBox it)
    {
        if(!it.isSelected())
        {
            destinos=0;
            for(javax.swing.JCheckBox it2:boxes)
            {
                it2.setSelected(false);
                ruta.clear();
                it2.setText("");
            }
        }
        if(destinos==0 && it.isSelected())
        {
            it.setText("Origen");
            ruta.add(enlace.get(it));
            destinos++;
        }
        else if(it.isSelected())
        {
            it.setText("Destino "+destinos);
            ruta.add(enlace.get(it));
            destinos++;
        }
    }
